
public class Punto2D {
	private float x;
	private float y;
	private char letra;
	
	public Punto2D(float x, float y, char letra) {
		super();
		this.x = x;
		this.y = y;
		this.letra = letra;
	}

	public float getX() {
		return x;
	}

	public void setX(float x) {
		this.x = x;
	}

	public float getY() {
		return y;
	}

	public void setY(float y) {
		this.y = y;
	}

	public char getLetra() {
		return letra;
	}

	public void setLetra(char letra) {
		this.letra = letra;
	}
	
	
}	
	
